n=float(input("Enter your marks/persantage: "))
if(n>=90 and n<=100):
    print("A Grade")
elif(n>=80 and n<90):
    print("B Grade")
elif(n>=70 and n<80):
    print("C Grade")
elif(n>=60 and n<70):
    print("D Grade")
elif(n>=50 and n<60):
    print("Average")
elif(n>=40 and n<50):
    print("Poor")
else:
    print("Fail \nNext Try Good Luck")
